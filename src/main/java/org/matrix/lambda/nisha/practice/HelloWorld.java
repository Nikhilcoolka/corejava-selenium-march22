package org.matrix.lambda.nisha.practice;

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World..........");
    }
}




/*

---------------Meaning of java words-------------------

# class = This keyword is used to declare a class in java.

# public = This keyword is an access modifier that represents visibility. It means it is visible to all.

# static =  It is a keyword. If we declare any method as static, it is known as the static method.
            The core advantage of the static method is that there is no need to create an object to
            invoke the static method. The main() method is executed by the JVM, so it doesn't require
            creating an object to invoke the main() method. So, it saves memory.

# void = It is the return type of the method. It means it doesn't return any value.


*/


