package org.matrix.lambda.nisha;

public class TestVariables {

    public static void main(String[] args) {
        int n = 7;
        /*
        int= Primitive datatype
        n= Variable
        7= Value
         */

        String str = "Singam";
        /*
        String= Non-primitive datatype/Class
        str= Variable
        Singam= Value
         */
        System.out.println(n);
        System.out.println(str);
    }
}



