package org.matrix.alpha.kamlesh.basics;

public class Switch {

    public static void main(String[] args) {

        int i = 20;
        switch (i){

            case 0:
                System.out.println("a");
                break;
            case 1:
                System.out.println("b");
                break;
            case 2:
                System.out.println("c");
                break;
            default:
                System.out.println("I");
        }
    }
}
