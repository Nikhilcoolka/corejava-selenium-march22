package org.matrix.alpha.nikhilesh.NewVari;

public class NewClassVariables {
    public static void main(String[] args) {
        int i =20;
        int j = 40;
        int k =50;
        int total;
        total=i+j+k;
        System.out.println("Add="+total);
        total=i-j-k;
        System.out.println("Sub="+total);
        total=i*j*k;
        System.out.println("Mult="+total);
        total=i%j%k;
        System.out.println("Div="+total);
    }
}
