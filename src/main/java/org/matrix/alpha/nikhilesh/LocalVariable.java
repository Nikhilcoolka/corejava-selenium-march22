public class LocalVariable {
    public static void main(String[] args) {
        int i = 10;
        float f = 6.7f;
        double b = 20.65f;
        long lg = 200;
        System.out.println(i);
        System.out.println(f);
        System.out.println(b);
        System.out.println(lg);
    }
}